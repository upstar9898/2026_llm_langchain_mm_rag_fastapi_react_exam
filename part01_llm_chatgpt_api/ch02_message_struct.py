# ─────────────────────────────────────────────────────────────────
# 예제 01: messages 구조와 세 가지 역할
# - API 호출 없이 messages 리스트의 구조를 이해합니다
# - 이 구조는 이 강의 전체에서 계속 사용됩니다
# ─────────────────────────────────────────────────────────────────

# messages는 딕셔너리의 리스트입니다
# 각 딕셔너리는 반드시 "role"과 "content" 키를 가져야 합니다
messages = [
    # ── 역할 1: system ───────────────────────────────────────────
    # AI의 정체성(페르소나)을 설정합니다
    # 대화 내내 유지되는 "배경 설명"입니다
    # → "넌 이런 AI야, 이렇게 행동해"
    {
        "role": "system",
        "content": "당신은 AI CCTV 보안 분석 시스템입니다. "
                   "OpenCV로 탐지된 객체 정보를 분석하여 위험도를 판단합니다. "
                   "답변은 항상 한국어로, 간결하게 작성합니다."
    },

    # ── 역할 2: user ─────────────────────────────────────────────
    # 사람이 AI에게 보내는 메시지입니다
    # → "내가 물어보는 내용"
    {
        "role": "user",
        "content": "새벽 2시에 창고 출입구에서 사람 2명이 탐지됐습니다."
    },

    # ── 역할 3: assistant ────────────────────────────────────────
    # AI가 이전에 답변한 내용입니다
    # → 멀티턴 대화에서 "AI의 이전 답변"을 기록할 때 씁니다
    # → 첫 번째 호출에서는 보통 포함하지 않습니다
    {
        "role": "assistant",
        "content": "위험도: 주의. 심야 시간대 2인 탐지. 경비 확인 요청."
    },

    # 그 다음 user 차례로 이어집니다
    {
        "role": "user",
        "content": "10분 뒤에 같은 장소에서 3명으로 늘었어요."
    },
]

# ── 구조 출력 ─────────────────────────────────────────────────────
print("=== messages 구조 ===\n")

# 역할별 아이콘 딕셔너리 — .get(key, 기본값) 패턴
role_icons = {
    "system":    "⚙️  SYSTEM   ",  # 배경 설정
    "user":      "🧑 USER     ",  # 사람의 질문
    "assistant": "🤖 ASSISTANT",  # AI의 답변
}

for i, msg in enumerate(messages):
    # enumerate: (인덱스, 값) 쌍 반환. start=0이 기본값
    icon = role_icons.get(msg["role"], "❓ UNKNOWN  ")
    print(f"  [{i}] {icon} : {msg['content'][:40]}...")
    #                                               ↑ 긴 content는 40자만 표시

print()
print("  💡 API는 이 리스트를 통째로 받아서 다음 assistant 답변을 생성합니다.")
print("  💡 API 자체는 '기억'이 없습니다 — 매번 전체 리스트를 전달해야 합니다.")